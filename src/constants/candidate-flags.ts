export type FilemakerMappingKey = (typeof FILEMAKER_MAPPING_KEYS)[number];

export function getFlagEnum<K extends FlagName>(flagName: K): readonly string[] {
  return FLAG_DEFINITIONS[flagName];
}

export function isValidFlagValue<K extends FlagName>(
  flagName: K,
  value: unknown
): value is FlagValue<K> {
  if (typeof value !== "string") return false;
  return (FLAG_DEFINITIONS[flagName] as readonly string[]).includes(value);
}

